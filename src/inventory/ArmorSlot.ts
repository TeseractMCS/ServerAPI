/**
 * Enum representing the different armor slots available in the game.
 */
enum ArmorSlot {
    /**
     * Represents the chest armor slot.
     */
    Chest = "Chest",
    /**
     * Represents the feet armor slot.
     */
    Feet = "Feet",
    /**
     * Represents the head armor slot.
     */
    Head = "Head",
    /**
     * Represents the legs armor slot.
     */
    Legs = "Legs",
}

export default ArmorSlot;
